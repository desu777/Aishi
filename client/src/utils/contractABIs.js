// Deployment info loaded from environment variables
export const DEPLOYMENT_INFO = {
  gravityScoreSystem: process.env.REACT_APP_GRAVITY_SCORE_SYSTEM,
  tokenFactory: process.env.REACT_APP_TOKEN_FACTORY,
  usdt: process.env.REACT_APP_USDT,
  treasury: process.env.REACT_APP_TREASURY,
  network: {
    name: process.env.REACT_APP_NETWORK_NAME,
    chainId: parseInt(process.env.REACT_APP_CHAIN_ID),
    rpcUrl: process.env.REACT_APP_RPC_URL
  }
};

// Validate environment variables
const validateEnvVars = () => {
  const missingVars = [];
  
  Object.entries(DEPLOYMENT_INFO).forEach(([key, value]) => {
    if (!value && key !== 'network') {
      missingVars.push(`REACT_APP_${key.toUpperCase()}`);
    }
  });
  
  if (DEPLOYMENT_INFO.network) {
    Object.entries(DEPLOYMENT_INFO.network).forEach(([key, value]) => {
      if (!value) {
        missingVars.push(`REACT_APP_${key.toUpperCase()}`);
      }
    });
  }
  
  if (missingVars.length > 0) {
    console.error(`Missing environment variables: ${missingVars.join(', ')}`);
  }
};

// Run validation
validateEnvVars();

